"""
pengulanaan while
program membaca buku
"""

#pengulangan while memiliki kelebihan untuk mengulang suatu kondisi nilai boolean

totalbuku = 100

bukusudahdibaca = 0

while bukusudahdibaca < totalbuku:
    bukusudahdibaca = bukusudahdibaca +1
    print(f"buku ke - {bukusudahdibaca} sudah di baca")

print(f"total{totalbuku} buku sudah dibaca")
